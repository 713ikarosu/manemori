'use client'

import { createClient } from '@/lib/supabase/client'
import { useRouter } from 'next/navigation'
import { useState } from 'react'
import Image from 'next/image'

export default function LoginPage() {
  const router = useRouter()
  const supabase = createClient()
  const [error, setError] = useState<string | null>(null)

  const handleGoogleLogin = async () => {
    setError(null)
    const { error: loginError } = await supabase.auth.signInWithOAuth({
      provider: 'google',
      options: {
        redirectTo: `${window.location.origin}/auth/callback`,
      },
    })

    if (loginError) {
      console.error('Error logging in:', loginError.message)
      setError('ログインに失敗しました。もう一度お試しください。')
    }
  }

  return (
    <div className="min-h-screen flex items-center justify-center bg-gradient-to-br from-background via-secondary to-background-elevated relative overflow-hidden">
      {/* Decorative background elements */}
      <div className="absolute inset-0 overflow-hidden pointer-events-none">
        <div className="absolute top-1/4 left-1/4 w-96 h-96 bg-primary/5 rounded-full blur-3xl animate-pulse" style={{ animationDuration: '4s' }} />
        <div className="absolute bottom-1/4 right-1/4 w-96 h-96 bg-accent/5 rounded-full blur-3xl animate-pulse" style={{ animationDuration: '6s', animationDelay: '1s' }} />
      </div>

      <div className="relative z-10 w-full max-w-md mx-4">
        {/* Main card */}
        <div className="bg-surface rounded-3xl border border-border shadow-xl p-10 backdrop-blur-sm animate-fade-in-scale">
          {/* Logo and branding */}
          <div className="text-center mb-10">
            <div className="mb-6 inline-block">
              <Image
                src="/manemori-logo_inline_trans.png"
                alt="まねもり"
                width={200}
                height={80}
                priority
                className="h-12 w-auto"
              />
            </div>
            <h1 className="text-3xl font-display font-medium text-foreground mb-3 tracking-tight">
              ようこそ
            </h1>
            <p className="text-foreground-muted leading-relaxed">
              日々の出費を記録し、<br />
              お金との関係をもっと心地よく
            </p>
          </div>

          {/* Error message */}
          {error && (
            <div className="mb-4 p-4 bg-status-error/10 border border-status-error/20 rounded-xl text-status-error text-sm text-center animate-fade-in">
              {error}
            </div>
          )}

          {/* Login button */}
          <button
            onClick={handleGoogleLogin}
            className="group w-full bg-surface hover:bg-primary border-2 border-border hover:border-primary text-foreground hover:text-white font-medium py-4 px-6 rounded-xl transition-smooth shadow-sm hover:shadow-md flex items-center justify-center gap-3"
          >
            <svg className="w-5 h-5 group-hover:scale-110 transition-transform" viewBox="0 0 24 24">
              <path
                fill="#4285F4"
                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              />
              <path
                fill="#34A853"
                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              />
              <path
                fill="#FBBC05"
                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              />
              <path
                fill="#EA4335"
                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              />
            </svg>
            <span className="font-medium">Googleでログイン</span>
          </button>

          {/* Features */}
          <div className="mt-10 pt-8 border-t border-border-subtle">
            <div className="space-y-4">
              {[
                { icon: 'M9 5H7a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2', text: 'シンプルな出費記録' },
                { icon: 'M9 19v-6a2 2 0 00-2-2H5a2 2 0 00-2 2v6a2 2 0 002 2h2a2 2 0 002-2zm0 0V9a2 2 0 012-2h2a2 2 0 012 2v10m-6 0a2 2 0 002 2h2a2 2 0 002-2m0 0V5a2 2 0 012-2h2a2 2 0 012 2v14a2 2 0 01-2 2h-2a2 2 0 01-2-2z', text: '予算との比較を可視化' },
                { icon: 'M8 7V3m8 4V3m-9 8h10M5 21h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v12a2 2 0 002 2z', text: 'カレンダーで振り返り' },
              ].map((feature, index) => (
                <div key={index} className="flex items-center gap-3 text-sm animate-fade-in" style={{ animationDelay: `${(index + 1) * 100}ms` }}>
                  <div className="w-8 h-8 rounded-lg bg-primary-subtle flex items-center justify-center flex-shrink-0">
                    <svg className="w-4 h-4 text-primary" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={feature.icon} />
                    </svg>
                  </div>
                  <span className="text-foreground-muted">{feature.text}</span>
                </div>
              ))}
            </div>
          </div>

          {/* Legal text */}
          <p className="text-xs text-foreground-muted text-center mt-8 leading-relaxed">
            ログインすることで、利用規約とプライバシーポリシーに<br />同意したものとみなされます
          </p>
        </div>
      </div>
    </div>
  )
}
